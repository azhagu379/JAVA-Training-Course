package TechnicalQuestion.Method;

public class BuildinMathFunction {
    public static void main(String[] args) {
        

        //build in Math function in java.

        System.out.println("Absolute value :"+Math.abs(-88));
        System.out.println("Round value :"+Math.round(7.89));
        System.out.println("Ceil value :"+Math.ceil(7.89));
        System.out.println("Floor value :"+Math.floor(7.89));
        
        
        System.out.println("Max value :"+Math.max(7.8,4));
        System.out.println("Min value :"+Math.min(7.8,4));

        System.out.println("Square root :"+Math.sqrt(9));
        System.out.println("Power :"+Math.pow(3,3));

        System.out.println("logarithm of :"+Math.log(22));
        System.out.println("log10 of :"+Math.log10(22));
        System.out.println("sine of :"+Math.sin(22));
        System.out.println("cosine of :"+Math.cos(22));
        System.out.println("Tangent :"+Math.tan(22));

    }
    
}
